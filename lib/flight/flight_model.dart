class Flight {
  final int id;
  final String from;
  final String to;
  final String date;
  final String time;
  final String image;
  final String company;
  final int ticketPrice;

  Flight({
    required this.id,
    required this.from,
    required this.to,
    required this.date,
    required this.time,
    required this.image,
    required this.company,
    required this.ticketPrice,
  });
}
